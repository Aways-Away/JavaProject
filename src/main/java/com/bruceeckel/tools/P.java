package com.bruceeckel.tools;

public class P {
    public static void rint(String s) {
        System.out.print(s);
    }

    public static void rintln(String s) {
        System.out.println(s);
    }

    public static void main(String[] args) {
        P.rint("liucui");
        P.rint("liubin");
        P.rintln("liucui");
        P.rintln("liubin");
    }

}
